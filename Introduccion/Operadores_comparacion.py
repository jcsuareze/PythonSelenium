# Operador de igualdad
print("Son iguales a y b?", a == b)
# Operador de desigualdad
print("Son diferentes a y b?", a != b)
# Operador de mayor que
print("Es a mayor que b?", a > b)
# Operador de menor que
print("Es a menor que b?", a < b)
# Operador de mayor o igual que
print("Es a mayor o igual que b?", a >= b)
# Operador de menor o igual que
print("Es a menor o igual que b?", a <= b)
# Operador de pertenencia
print("a esta en la lista [1,2,3]?", a in [1,2,3])
# Operador de no pertenencia
print("a no esta en la lista [1,2,3]?", a not in [1,2,3])
# Operador de identidad
print("a es b?", a is b)
# Operador de no identidad
print("a no es b?", a is not b)

# Output:
# Son iguales a y b? False
# Son diferentes a y b? True
# Es a mayor que b? True
# Es a menor que b? False
